<template>
  <base-modal
    v-if="inputIsInvalid"
    title="Input is invalid"
    @close="closeModal"
  >
    <template #default>
      <p>All inputs are required!</p>
    </template>
    <template #actions>
      <base-button @click="closeModal">Okay</base-button>
    </template>
  </base-modal>
  <base-card>
    <h2>Add Resource</h2>
    <form @submit.prevent="submitData">
      <div class="form-control">
        <label for="title">Title</label>
        <input type="text" name="title" id="title" ref="inputTitle" />
      </div>
      <div class="form-control">
        <label for="description">Description</label>
        <textarea
          name="description"
          id="description"
          rows="3"
          ref="inputDescription"
        ></textarea>
      </div>
      <div class="form-control">
        <label for="link">Link</label>
        <input type="url" name="link" id="link" ref="inputUrl" />
      </div>
      <div>
        <base-button type="submit">Add Resource</base-button>
      </div>
    </form>
  </base-card>
</template>

<script>
import BaseButton from '../UI/BaseButton.vue';
import BaseModal from '../UI/BaseModal.vue';
export default {
  components: { BaseModal, BaseButton },
  inject: ['addResource'],
  data() {
    return {
      inputIsInvalid: false,
    };
  },
  methods: {
    submitData() {
      const enteredTitle = this.$refs.inputTitle.value;
      const enteredDesc = this.$refs.inputDescription.value;
      const enteredLink = this.$refs.inputUrl.value;

      if (
        enteredTitle.trim() === '' ||
        enteredDesc.trim() === '' ||
        enteredLink.trim() === ''
      ) {
        this.inputIsInvalid = true;
        return;
      }

      this.addResource(enteredTitle, enteredDesc, enteredLink);
    },
    closeModal() {
      this.inputIsInvalid = false;
    },
  },
};
</script>

